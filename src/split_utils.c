#include "minishell.h"

void	free_mini_split(char **s, int j)
{
	int	i;

	i = 0;
	if (s != NULL)
	{
		while (i < j)
		{
			if (s[i])
				free(s[i]);
			i++;
		}
		free(s);
	}
}