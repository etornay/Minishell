int	prompt(t_data *p)
{
	p->shell = ft_strdup("PACOSHELL: ");
	if (p->shell == NULL)
		return (EXIT_FAILURE);
	p->line = readline(p->shell);
	free(p->shell);
	p->shell = NULL;
	if (p->line == NULL)
		return (EXIT_FAILURE);
	return (EXIT_SUCCESS);
}
